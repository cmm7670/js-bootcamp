let fahrenheit = 50

//calculate celsius and store in celsius variable
let celsius = (fahrenheit - 32) * (5/9)
//print that value
console.log('');
console.log(fahrenheit + ' degrees fahrenheit is ' + celsius + ' degrees celsius.');
console.log ('');

//calculate kelvin value and store in variable
let kelvin = celsius + 273.15
//print that value
console.log('');
console.log(fahrenheit + ' degrees fahrenheit is ' + kelvin + ' kelvin.');
console.log('');